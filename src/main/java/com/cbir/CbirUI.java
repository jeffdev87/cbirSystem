	private Vector<Distance> runQuery(ImageProc queryImg, DescriptorType descrCode, DistanceType distCode) {

		if (queryImg == null || descrCode == null || distCode == null)
			return null;

		OutputFileManager readFile = null;
		Vector<Distance> resultVec = null;

		switch (descrCode) {
		case HISTOGRAM:
			readFile = new OutputFileManager(histFileName);

			readFile.openFile();

			if (readFile.fileExists() && !readFile.emptyFile()) {

				resultVec = readFile.calcDistancias(queryImg.getHistograma(), queryImg.getImageName(), distCode);
				readFile.closeFile();
			} else {
				System.err.println("Error (hist): Features were not extracted.");
			}
			break;

		case HARALICK:
			readFile = new OutputFileManager(descrFileName);

			readFile.openFile();
			if (readFile.fileExists() && !readFile.emptyFile()) {

				resultVec = readFile.calcDistancias(queryImg.getVetCaracteristicas(), queryImg.getImageName(),
						distCode);
				readFile.closeFile();
			} else {
				System.err.println("Error (hara): Features were not extracted.");
			}
			break;

		default:
			break;
		}

		return resultVec;
	}

	private void btBuscarActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btBuscarActionPerformed

		Vector<Distance> vetDist = new Vector<Distance>();

		if (queryImg == null) {
			JOptionPane.showMessageDialog(null, "Specify a query image", "Validation error", JOptionPane.ERROR_MESSAGE);
			return;
		}

		if ((histFileName == null) || (descrFileName == null)) {
			JOptionPane.showMessageDialog(null, "Open a valid image base", "Validation error",
					JOptionPane.ERROR_MESSAGE);
			return;
		}

		DescriptorType descrCode = null;
		if (grupoOptExtrator.isSelected(opHist.getModel())) {
			descrCode = DescriptorType.HISTOGRAM;
		} else if (grupoOptExtrator.isSelected(opDescr.getModel())) {
			descrCode = DescriptorType.HARALICK;
		} else {
			JOptionPane.showMessageDialog(null, "Select a descriptor", "Validation error", JOptionPane.ERROR_MESSAGE);
			return;
		}

		DistanceType distCode = null;

		if (grupoDist.isSelected(opDistEucl.getModel())) {
			distCode = DistanceType.EUCLIDIAN;
		} else if (grupoDist.isSelected(opDistManhatan.getModel())) {
			distCode = DistanceType.MANHATAN;
		} else {
			JOptionPane.showMessageDialog(null, "Specify a distance function", "Validation error",
					JOptionPane.ERROR_MESSAGE);
			return;
		}

		if ((nImgsResp = txtNImgResp.getText()).equals("")) {
			JOptionPane.showMessageDialog(null, "Inform the number of images to be retrieved", "Validation error",
					JOptionPane.ERROR_MESSAGE);
			return;
		}

		vetDist = runQuery(queryImg, descrCode, distCode);

		if (vetDist != null && !vetDist.isEmpty()) {
			Collections.sort(vetDist, new DistanceComparator());

			Vector<String> imgNameResultSet = new Vector<String>();

			int nImgs = 0;
			try {
				nImgs = Integer.parseInt(nImgsResp);
			} catch (NumberFormatException e) {
				JOptionPane.showMessageDialog(null, "An invalid input was informed!", "Validation error",
						JOptionPane.ERROR_MESSAGE);
				return;
			}

			if (nImgs > vetDist.size())
				nImgs = vetDist.size();

			for (int i = 0; i < nImgs; i++)
				imgNameResultSet.add(vetDist.elementAt(i).imageNameBase);

			resultSet = imgNameResultSet;
			resultSetList.setListData(imgNameResultSet);

			frameResp.setVisible(true);
		} else {
			JOptionPane.showMessageDialog(null, "Error while querying the system.", "Query error",
					JOptionPane.ERROR_MESSAGE);
		}
	}// GEN-LAST:event_btBuscarActionPerformed

