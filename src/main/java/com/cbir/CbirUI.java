	private void menuExtratCaracActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_menuExtratCaracActionPerformed

		int numElemList = imageList.getModel().getSize();

		for (int i = 0; i < numElemList; i++) {
			String nomeArq = (String) imageList.getModel().getElementAt(i);
			String pathCompleto = path + SEP_SIMB + nomeArq;

			ImageHandler imgHandler = null;
			try {
				imgHandler = new ImageHandler(pathCompleto);
			} catch (IOException ex) {
				System.err.println(ex.getMessage());
			}

			ImageProc procImgLocal = ImageProcFactory.procImageFactoryMethod(imgHandler);

			if (!arqHistSet.contains(nomeArq)) {
				if (procImgLocal.calcHistograma()) {
					histFile.addEntry(nomeArq + ":" + procImgLocal.getHistogramaString());
					arqHistSet.add(nomeArq);
				} else {
					System.err.println("Error: Histogram was not calculated!");
				}
			} else {
				System.out.println("There is already a calculated histogram for " + nomeArq + "!");
			}

			if (!arqDescrSet.contains(nomeArq)) {
				if (procImgLocal.calcCaracteristicas()) {
					descrFile.addEntry(nomeArq + ":" + procImgLocal.getVetCaractString());
					arqDescrSet.add(nomeArq);
				} else {
					System.err.println("Error: Features vector was not " + "calculated!");
				}
			} else {
				System.out.println("There is already a calculated feature vector for " + nomeArq + "!");
			}
		}

		JOptionPane.showMessageDialog(null, "Features extracted successfully.", "Operation finished",
				JOptionPane.INFORMATION_MESSAGE);

		histFile.closeFile();
		descrFile.closeFile();
	}// GEN-LAST:event_menuExtratCaracActionPerformed

